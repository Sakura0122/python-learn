"""
    关系运算符比较两个值，并返回一个布尔值。
    == 判断两个值是否相等
    != 判断两个值是否不相等
    > 判断左边的值是否大于右边的值
    < 判断左边的值是否小于右边的值
    >= 判断左边的值是否大于或等于右边的值
    <= 判断左边的值是否小于或等于右边的值
"""
a = 10
b = 20
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

print('-' * 20)
# python中的字符串也可以比较
print('abcd' > 'abce')
